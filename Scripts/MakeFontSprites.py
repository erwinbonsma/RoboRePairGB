
ch = {
    False: "5",
    True: "4"
}

for i in range(32):
    print("  0x{1}{0}, 0x{0}{1},".format(ch[i & 0x01 == 0x01], ch[False]))
    print("  0x{0}{1}, 0x{2}{3},".format(
        ch[(i & 0x08) == 0x08],
        ch[(i & 0x10) == 0x10 or (i & 0x09) == 0x09],
        ch[(i & 0x10) == 0x10 or (i & 0x03) == 0x03],
        ch[(i & 0x02) == 0x02]
    ))
    print("  0x{0}{1}, 0x{2}{3},".format(
        ch[(i & 0x08) == 0x08],
        ch[(i & 0x10) == 0x10 or (i & 0x0c) == 0x0c],
        ch[(i & 0x10) == 0x10 or (i & 0x06) == 0x06],
        ch[(i & 0x02) == 0x02]
    ))
    print("  0x{1}{0}, 0x{0}{1},".format(ch[i & 0x04 == 0x04], ch[False]))
    print()